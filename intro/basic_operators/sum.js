// Suma de números
let suma = 5 + 3;
console.log(suma); // 8

// Concatenación de strings
let nombre = "Jaime"
let saludo = "Hola, " + nombre + "!";
console.log(saludo); // Hola, Jaime!

// Cuidado al sumar numeros con strings
console.log(5 + "3"); // 53 (lo trata como string)
console.log("5" + 3); // 53 (lo trata como string)