/* 
Se usan comillas simples, dobles o backticks para definir un string 
*/

let nombre = "Jaime"; // Comillas dobles
let apellido = 'Alvarado'; // Comillas simples
let saludo = `Hola, mi nombre es ${nombre} ${apellido}`; // Backticks

console.log(nombre); // Jaime
console.log(apellido); // Alvarado
console.log(saludo); // Hola, mi nombre es Jaime Alvarado
console.log(typeof nombre); // string